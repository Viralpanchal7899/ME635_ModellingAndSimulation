i = 1;
N = zeros();
for x = 0:0.01:1
N_0 = ((x-2)*(x-3)*(x-4)*(x-5))/24;
N(i,1) = N_0;
N_1 = -((x-1)*(x-3)*(x-4)*(x-5))/6;
N(i,2) = N_1;
N_2 = ((x/2 - 1/2)*(x-2)*(x-4)*(x-5))/2;
N(i,3) = N_2;
N_3 = -((x/3 - 1/3)*(x-2)*(x-3)*(x-5))/2;
N(i,4) = N_3;
N_4 = ((x/4 - 1/4)*(x-2)*(x-3)*(x-4))/6;
N(i,5) = N_4;
N(i,6) = x;
i = i+1;
end

plot(N(:,6),N(:,1))
hold on
plot(N(:,6),N(:,2))
hold on 
plot(N(:,6),N(:,3))
hold on 
plot(N(:,6),N(:,4))
